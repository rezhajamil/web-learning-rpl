     * Display the specified resource.
     *
     * @param  \App\Models\Subject  $subject
     * @return \Illuminate\Http\Response
     */
    public function show(Subject $subject)
    {
        $subject = Subject::with(['lesson', 'example', 'quiz', 'other'])->find($subject->id);
        $answer = QuizAnswer::where('user_id', auth()->user()->id)->where('quiz_id', $subject->quiz_id)->first();
        $answer = $answer ? $answer : null;
        return view('user.subject.show', [
            'title' => $subject->name,
            'subject' => $subject,
            'answer' => $answer,
        ]);
    }

